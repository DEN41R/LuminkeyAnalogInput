function drawStick(ctx, x, y) {
    ctx.clearRect(0, 0, 150, 150);
    
   
    ctx.strokeStyle = '#333';
    ctx.lineWidth = 1;
    ctx.beginPath(); ctx.moveTo(75, 0); ctx.lineTo(75, 150); ctx.stroke();
    ctx.beginPath(); ctx.moveTo(0, 75); ctx.lineTo(150, 75); ctx.stroke();
    
  
    ctx.beginPath(); ctx.arc(75, 75, 75, 0, Math.PI*2); ctx.stroke();
    
  
    const px = 75 + (x * 75);
    const py = 75 - (y * 75); 
    ctx.fillStyle = '#3b82f6';
    ctx.beginPath(); ctx.arc(px, py, 10, 0, Math.PI*2); ctx.fill();
}


let lastRawData = null;

function renderTester(data) {
    if (!document.getElementById('tab-tester').classList.contains('active')) return;
    drawStick(lsCtx, data.lx, data.ly);
    drawStick(rsCtx, data.rx, data.ry);
    
    ltFill.style.width = (data.lt * 100) + '%';
    rtFill.style.width = (data.rt * 100) + '%';
    
    btnList.innerHTML = '';
    data.buttons.forEach(b => {
        const el = document.createElement('div');
        el.className = 'active-btn';
        el.innerText = b;
        btnList.appendChild(el);
    });
    
    let rawBox = document.getElementById('debug-raw-keys');
    if (!rawBox) {
        rawBox = document.createElement('div');
        rawBox.id = 'debug-raw-keys';
        rawBox.style = 'margin-top: 20px; padding: 10px; background: #222; border-radius: 8px; color: #aaa; font-family: monospace; font-size: 12px;';
        document.querySelector('#tab-tester').appendChild(rawBox);
    }
    if (data.raw_keys && Object.keys(data.raw_keys).length > 0) {
        let s = `<b>${i18n[currentLang]['test_raw']}</b><br>`;
        for (const [k, v] of Object.entries(data.raw_keys)) {
            s += k + ': ' + (v * 100).toFixed(1) + '% ';
        }
        rawBox.innerHTML = s;
    } else {
        rawBox.innerHTML = `<b>${i18n[currentLang]['test_raw']}</b> ${i18n[currentLang]['test_raw_empty']}`;
    }
}

function renderCurveLiveIndicator(data) {
    if (!document.getElementById('tab-config').classList.contains('active')) return;
    
    let maxRaw = 0.0;
    if (data.raw_keys) {
        for (const val of Object.values(data.raw_keys)) {
            if (val > maxRaw) maxRaw = val;
        }
    }
    
   
    drawCurve();
    
    if (maxRaw > 0.01) {
        const xCanvas = maxRaw * 300;
        ctx.strokeStyle = '#ef4444';
        ctx.lineWidth = 2;
        ctx.setLineDash([5, 5]);
        ctx.beginPath();
        ctx.moveTo(xCanvas, 0);
        ctx.lineTo(xCanvas, 200);
        ctx.stroke();
        ctx.setLineDash([]);
        
        ctx.fillStyle = '#ef4444';
        ctx.font = '12px Arial';
        ctx.fillText((maxRaw * 4.0).toFixed(2) + 'mm', xCanvas + 5, 20);
    }
}

function updateLiveData() {
    if (!window.pywebview) return;
    const isTester = document.getElementById('tab-tester').classList.contains('active');
    const isConfig = document.getElementById('tab-config').classList.contains('active');
    if (!isTester && !isConfig) return;
    
    window.pywebview.api.get_live_data().then(data => {
        if (!data) return;
        lastRawData = data;
        if (isTester) renderTester(data);
        if (isConfig) renderCurveLiveIndicator(data);
    });
}



