let currentLang = "ru";

function setLanguage(lang) {
    currentLang = lang;
    currentLang = lang;
    const dict = i18n[lang];
    if(!dict) return;
    
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) {
            if (el.tagName.toLowerCase() === 'span' && el.innerHTML.includes('<b')) {
              
                el.innerHTML = dict[key];
            } else {
                el.innerText = dict[key];
            }
        }
    });
    
    
    document.querySelectorAll('.driver-dl-btn').forEach(btn => {
        btn.innerText = dict["btn_download"];
    });
    renderAdvanced();
}

