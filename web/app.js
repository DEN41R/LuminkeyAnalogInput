function saveConfig() {
    if (!window.pywebview) return;
    
    
    const lang = document.getElementById("lang-select").value;
    const savedCurve = curvePoints.map(p => ({x: (p.x/300)*4.0, y: 1.0 - (p.y/200)}));
    
    const config = {
        enabled: document.getElementById('toggle-controller').checked,
        gamepad_mode: !document.getElementById('toggle-keyboard').checked,
        square_vector: document.getElementById('toggle-square').checked,
        angle_snapping: toggleAngle.checked ? parseInt(angleSlider.value) : 0,
        snap_tap_items: snapTapItems,
        rt_items: rtItems,
        dks_items: dksItems,
        language: lang,
        curve_points: savedCurve,
        
        bindings: bindings
    };
    
    window.pywebview.api.save_config(config);
}


