let draggedBtn = null;
let bindings = {};

document.querySelectorAll('.draggable').forEach(btn => {
    btn.setAttribute('draggable', true);
    btn.addEventListener('dragstart', (e) => {
        draggedBtn = btn.dataset.btn;
        e.dataTransfer.setData('text/plain', draggedBtn);
        e.dataTransfer.effectAllowed = 'copy';
    });
});

document.querySelectorAll('.key').forEach(key => {
    key.addEventListener('dragover', (e) => { e.preventDefault(); key.classList.add('drag-over'); });
    key.addEventListener('dragleave', (e) => { key.classList.remove('drag-over'); });
    key.addEventListener('drop', (e) => {
        e.preventDefault();
        key.classList.remove('drag-over');
        if (draggedBtn) {
            const keyName = key.dataset.key;
            bindings[keyName] = draggedBtn;
            updateKeyVisual(key, draggedBtn);
            saveConfig();
        }
    });
    key.addEventListener('contextmenu', (e) => {
        e.preventDefault();
        const keyName = key.dataset.key;
        if (bindings[keyName]) {
            delete bindings[keyName];
            removeKeyVisual(key);
            saveConfig();
        }
    });
});

function updateKeyVisual(keyElement, btnName) {
    keyElement.classList.add('bound');
    let badge = keyElement.querySelector('.key-badge');
    if (!badge) {
        badge = document.createElement('div');
        badge.className = 'key-badge';
        keyElement.appendChild(badge);
    }
    
   
    const originalBtn = document.querySelector(`.g-btn[data-btn="${btnName}"]`);
    if (originalBtn) {
        badge.innerHTML = originalBtn.innerHTML;
       
        badge.style.display = 'flex';
        badge.style.justifyContent = 'center';
        badge.style.alignItems = 'center';
        badge.style.transform = 'scale(0.6)';
        badge.style.transformOrigin = 'bottom right';
        badge.style.pointerEvents = 'none'; 
    } else {
        badge.innerText = btnName;
    }
}

function removeKeyVisual(keyElement) {
    keyElement.classList.remove('bound');
    const badge = keyElement.querySelector('.key-badge');
    if (badge) badge.remove();
}


const canvas = document.getElementById('curveCanvas');
const ctx = canvas.getContext('2d');
const dzVal = document.getElementById('dzVal');
const maxVal = document.getElementById('maxVal');


let curvePoints = [
    { x: (0.1 / 4.0) * 300, y: 200 },
    { x: (1.5 / 4.0) * 300, y: 150 },
    { x: (2.5 / 4.0) * 300, y: 50 },
    { x: (4.0 / 4.0) * 300, y: 0 }
];
let draggingPointIdx = -1;

function getMousePos(evt) {
    const rect = canvas.getBoundingClientRect();
    return {
        x: evt.clientX - rect.left,
        y: evt.clientY - rect.top
    };
}

canvas.addEventListener('mousedown', (e) => {
    const pos = getMousePos(e);
    draggingPointIdx = -1;
    for (let i = 0; i < curvePoints.length; i++) {
        if (Math.hypot(pos.x - curvePoints[i].x, pos.y - curvePoints[i].y) < 15) {
            draggingPointIdx = i;
            break;
        }
    }
});

canvas.addEventListener('mousemove', (e) => {
    if (draggingPointIdx !== -1) {
        const pos = getMousePos(e);
        
        let minX = 0;
        let maxX = 300;
        
        if (draggingPointIdx > 0) minX = curvePoints[draggingPointIdx - 1].x + 5;
        if (draggingPointIdx < curvePoints.length - 1) maxX = curvePoints[draggingPointIdx + 1].x - 5;
        
        curvePoints[draggingPointIdx].x = Math.max(minX, Math.min(maxX, pos.x));
        curvePoints[draggingPointIdx].y = Math.max(0, Math.min(200, pos.y));
        
        drawCurve();
    }
});

window.addEventListener('mouseup', () => {
    if (draggingPointIdx !== -1) {
        draggingPointIdx = -1;
        saveConfig();
    }
});

function drawCurve() {
    ctx.clearRect(0, 0, canvas.width, canvas.height);
    
    const dz = (curvePoints[0].x / 300) * 4.0;
    const maxZ = (curvePoints[3].x / 300) * 4.0;
    dzVal.innerText = dz.toFixed(2);
    maxVal.innerText = maxZ.toFixed(2);
    
   
    ctx.strokeStyle = '#333';
    ctx.lineWidth = 1;
    for(let i=1; i<4; i++) {
        ctx.beginPath(); ctx.moveTo(i * 75, 0); ctx.lineTo(i * 75, 200); ctx.stroke();
        ctx.beginPath(); ctx.moveTo(0, i * 50); ctx.lineTo(300, i * 50); ctx.stroke();
    }
    
   
    ctx.fillStyle = 'rgba(59, 130, 246, 0.4)';
    ctx.strokeStyle = '#3b82f6';
    ctx.lineWidth = 3;
    
    ctx.beginPath();
    ctx.moveTo(0, 200);
    ctx.lineTo(curvePoints[0].x, 200); 
    
   
    ctx.moveTo(0, curvePoints[0].y);
    for (let i = 0; i < curvePoints.length; i++) {
        ctx.lineTo(curvePoints[i].x, curvePoints[i].y);
    }
    ctx.lineTo(300, curvePoints[3].y);
    ctx.stroke();
    

    ctx.lineTo(300, 200);
    ctx.lineTo(0, 200);
    ctx.closePath();
    ctx.fill();
    
  
    ctx.fillStyle = '#fff';
    ctx.strokeStyle = '#3b82f6';
    ctx.lineWidth = 2;
    for (let i = 0; i < curvePoints.length; i++) {
        ctx.beginPath(); ctx.arc(curvePoints[i].x, curvePoints[i].y, 6, 0, Math.PI*2); ctx.fill(); ctx.stroke();
    }
}


